package com.zzx.sys.control.util;

import java.util.UUID;

/**
 * SID生成器
 * @author lsjun6
 * @version 1.0.0
 */
public class SIDCreator {

	/**
	 * 获取随机UUID
	 * @return 随机UUID
	 */
	public static String getRandomSid() {

		UUID uuid = UUID.randomUUID();
		return uuid.toString().toUpperCase().replaceAll("-", "").replaceAll(" ", "");

	}
}
